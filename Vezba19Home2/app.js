number = Number(prompt("Unesite broj"));
if (number === 1) {
  console.log("Danas je ponedeljak");
} else if (number === 2) {
  console.log("Danas je utorak");
} else if (number === 3) {
  console.log("Danas je sreda");
} else if (number === 4) {
  console.log("Danas je cetvrtak");
} else if (number === 5) {
  console.log("Danas je petak");
} else if (number === 6) {
  console.log("Danas je subota");
} else if (number === 7) {
  console.log("Danas je nedelja");
} else {
  console.log("Uneli ste pogresan broj, molim vas da unesete broj od 1 do 7");
}
